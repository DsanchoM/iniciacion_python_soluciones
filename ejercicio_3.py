def main():
    width = 17
    height = 12.0

    print(f'width/2: {width/2}, type: {type(width/2)}')
    print(f'width/2.0: {width/2.0}, type: {type(width/2.0)}')
    print(f'height/3: {height/3}, type: {type(height/3)}')
    print(f'1+2*5: {1+2*5}, type: {type(1+2*5)}')

if __name__ == "__main__":
    main()